    private void Btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_guardarActionPerformed
        String descripcion = txt_descripcion.getText().trim();
        Ctrl_Categoria controlCategoria = new Ctrl_Categoria();

        if (descripcion.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Complete todos los campos.");
            return;
        }
        try {
            if (!controlCategoria.existeCategoria(descripcion)) {
                Categoria categoria = new Categoria();
                categoria.setDescripcion(descripcion);
                categoria.setEstado(1);

                if (controlCategoria.guardar(categoria)) {
                    JOptionPane.showMessageDialog(null, "Registro guardado con éxito.");
                } else {
                    JOptionPane.showMessageDialog(null, "Error al guardar la categoría.");
                }
            } else {
                JOptionPane.showMessageDialog(null, "La categoría ya existe.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error inesperado: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            // Limpiar campo después de guardar o intentar guardar
            txt_descripcion.setText("");
        }
    }//GEN-LAST:event_Btn_guardarActionPerformed

